data(2,2)= str2double(get(handles.edit9,'String'));
data(2,3)= str2double(get(handles.edit10,'String'));
data(2,4)= str2double(get(handles.edit11,'String'));
data(3,1)= str2double(get(handles.edit13,'String'));
data(3,2)= str2double(get(handles.edit14,'String'));
data(3,3)= str2double(get(handles.edit15,'String'));
data(3,4)= str2double(get(handles.edit16,'String'));

%memanggil fungsi WP input data output hasil
[hasil] = WP(data);
%mensorting data 
[M I] = sort(hasil,'descend');

%menampilkan nama paket data dan hasil sesuai urutan peringkat
set(handles.text14, 'String',namaPaket(I(1)));
set(handles.text15, 'String',namaPaket(I(2)));
set(handles.text16, 'String',namaPaket(I(3)));

set(handles.text17, 'String',M(1));
set(handles.text18, 'String',M(2));
set(handles.text19, 'String',M(3));

