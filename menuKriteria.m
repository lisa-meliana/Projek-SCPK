% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global w;
global k;
%mengambil nilai dari bobot kriteria yang dipilih
kPertama = get(handles.listbox1,'value');
kDua = get(handles.listbox2,'value');
kTiga = get(handles.listbox3,'value');
kEmpat = get(handles.listbox4,'value');
k = [1,0,1,1];
%menggabungkan bobot dan dilakukan normalisasi
w=[kPertama, kDua, kTiga,kEmpat];
w=w./sum(w);

%untuk kriteria cost bobot dikali -1
for j=1:4
    if k(j)==0 
        w(j)=-1*w(j);
    end
end

%mengambil nilai dari listbox untuk jumlah paketdata yang akan dibandingkan
pilihan = get(handles.listbox8,'value');

%menghubungkan ke gui lain, sesuai jumlah paket data
if pilihan == 1
    WP_3Data
    close('menuKriteria');
elseif pilihan == 2
    WP_4Data
    close('menuKriteria');  
elseif pilihan == 3
    WP_5Data
    close('menuKriteria'); 
end

